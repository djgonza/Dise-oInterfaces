		<p>El primer paso es cocer los boniatos. Los pelamos y cortamos en trozos y ponemos en el agua fría hasta que hierva y dejamos unos 20 minutos. Comprobamos que estén listos: que estén blandos y se partan fácilmente.</p><p>Escurrimos los boniatos y machacamos bien con el tenedor. Veréis que queda una masa cremosa a la que no le hace falta nada más que un poco de sal.</p><p>Por otro lado iremos preparando los champiñones. Los cortamos en trocitos bien pequeños y ponemos freír.Después los mezclamos con el puré de boniatos y dejamos enfriar igual que haríamos con la bechamel de las croquetas. Yo siempre lo dejo reposar estirado en un plato llano.</p><p>El siguiente paso es hacer las bolitas y rebozar en un poco de agua o leche vegetal y pan rallado. Si queréis que quede bien crujiente repetid el proceso de rebozado dos veces.</p><p>Ya sólo queda freirías en aceite bien caliente y escurrir después en papel de cocina. Las servimos y ¡a disfrutarlas!</p>
