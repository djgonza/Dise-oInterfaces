	<section class="text-justify">
		<h4 class="c-verde">Modo de Realización</h4>
		<p>Lavamos y picamos la cebolla, el calabaciín y el puerro en dados pequeños. Después sofreímos la cebolla y el puerro a fuego lento hasta que la cebolla empiece a estar transparente. Después añadimos el calabacín y dejamos un par de minutos. Añadimos el arroz y removemos hasta que se quite el blanco y empiece a estar un poco translucido (removiendo con cuidado para que no se pegue).</p><p>El siguiente paso es añadir el vino. Dejaremos que el arroz lo absorva y después iremos añadiendo el caldo de verduras bien caliente poco a poco: echamos un cucharón de caldo, dejamos que absorba y añadimos el siguente, así hasta terminar el caldo. Siempre removiendo frecuentemente y con el fuego bajo. Recordad salpimentar y añadir la albahaca.</p><p>Cuando veamos que ya queda poco para que el risotto esté listo, batimos el yogur de soja para que quede bien suave y lo añadimos al arroz. Estará listo cuando notemos que no queda líquido, pero sí cremoso, blando por fuera y un poco duro por dentro.</p><p>¡Que aproveche!</p>
