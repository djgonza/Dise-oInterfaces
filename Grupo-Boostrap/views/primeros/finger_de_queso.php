		<h4 class="c-verde">Modo de Realización</h4>
		<p>En primer lugar cortaremos la mozzarrella en bastones. Después añadiremos al plato en el que tengamos el panko una pizca de sal, media cucharilla de ajo en polvo, una pizca de ajo en polvo y perejil al gusto. Lo mezclamos y reservamos.</p><p>Después prepararemos la tempura añadiendo agua fría (de la nevera) a nuestra harina hasta tener una masa más densa de lo que sería un huevo batido.</p><p>Tan pronto tengamos la tempura hundimos los fingers de queso vegano en ella y después rebozamos en el panko. Tienen que quedar perfectamente cubiertos. Repetimos el proceso con todos los fingers y después metemos al congelador al menos una hora.</p><p>Es importante hacerlo así para que luego los fingers no se nos deshagan al freír.</p><p>Sacamos del congelador cuando tengamos ya el aceite para freírlos bien caliente. Hay que freírlos con aceite abundante y durante poquito tiempo, simplemente que el rebozado dore y sacamos a un un plato con papel de cocina para que pueda absorber un posible exceso de aceite.</p><p>Podemos acompañar con salsa barbacoa, ketchup o solos… ¡están riquísimos, crujientes por fuera y muy suaves por dentro!</p><p>¡Que aprovechen los fingers de queso veganos!</p>
