	<section class="col-md-5">
		<h4 class="c-verde">Ingredientes</h4>
		<ul>
			<li>½ calabaza cacahuete (650 g ó 1,5 libras)</li>
			<li>2 zanahorias</li>
			<li>1 rama de apio</li>
			<li>1 puerro</li>
			<li>½ cebolla</li>
			<li>3 dientes de ajo</li>
			<li>2 tazas de agua (500 ml)</li>
			<li>1 cucharada de aceite de oliva virgen extra (opcional)</li>
			<li>1 cucharadita de sal</li>
			<li>1 cucharadita de cúrcuma</li>
			<li>1 cucharadita de comino molido</li>
			<li>⅛ de cucharadita de pimienta negra molida</li>
		</ul>
	</section>
	<section class="text-justify">
		<h4 class="c-verde">Preparación</h4>
		<p>Pelamos y troceamos las verduras.</p>
		<p>Echamos las verduras y el agua en una olla y cuando rompa a hervir cocinamos a fuego medio alto durante unos 15 ó 20 minutos o hasta que las verduras estén tiernas.</p>
		<p>Echamos el agua, las verduras y el resto de ingredientes en una batidora y batimos hasta que estén bien integrados.</p>
		<p>Nosotros para decorar le echamos unas zanahorias salteadas, nueces troceadas y semillas de sésamo.</p>
		<p>Podéis guardarla en la nevera en un recipiente hermético durante unos 4 ó 5 días o congelarla sin problemas.</p>
	</section>
