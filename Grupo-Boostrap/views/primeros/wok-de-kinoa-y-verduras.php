	<section class="col-md-5">
		<h4 class="c-verde">Ingredientes</h4>
		<ul>
			<ul>
				<li>1 taza de quinoa (200 g)</li>
				<li>½ cebolla</li>
				<li>½ pimiento rojo</li>
				<li>1 taza de lombarda (70 g)</li>
				<li>1 zanahoria</li>
				<li>½ cabeza de brócoli</li>
				<li>2 cucharadas de aceite de oliva virgen extra</li>
				<li>4 dientes de ajo</li>
				<li>¼ cucharadita de cayena en polvo</li>
				<li>½ cucharadita de jengibre en polvo</li>
				<li>1 cucharada de tamari o salsa de soja</li>
				<li>1 cucharada de panela molida o azúcar moreno o de coco</li>
				<li>Semillas de sésamo</li>
			</ul>
		</section>
		<section class="text-justify">
			<h4 class="c-verde">Modo de Realización</h4>
			<p>Troceamos la cebolla, el pimiento, la lombarda y la zanahoria en juliana.</p>
			<p>Troceamos la cabeza de brócoli en trocitos más pequeños usando sólo las flores y descartando el tallo.</p>
			<p>Cocinamos las verduras al vapor o en abundante agua hirviendo durante un par de minutos para que se cocinen ligeramente, pero sigan estando crujientes. Colamos y reservamos.</p>
			<p>En una sartén grande o wok calentamos el aceite y doramos los dientes de ajo troceados durante un par de minutos a fuego medio alto.</p>
			<p>Echamos la cayena y las verduras y las doramos durante otro par de minutos, removiendo de vez en cuando.</p>
			<p>Echamos la quinoa y el resto de ingredientes (salvo las semillas de sésamo) y cocinamos durante otros 2 minutos más.</p>
			<p>Servimos con semillas de sésamo por encima (opcional).</p>
			<p>Podemos guardarlo en la nevera en un recipiente hermético durante unos 5 días.</p>
		</section>
