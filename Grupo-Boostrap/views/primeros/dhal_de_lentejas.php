	<h4 class="c-verde">Ingredientes</h4>
		<ul>
			<li>150g de lentejas</li>
			<li>1 cebolla</li>
			<li>250g de tomate pelado en dados (o triturado)</li>
			<li>3g de jengibre (aproximadamente)</li>
			<li>1 cucharadita de comino en grano</li>
			<li>1 cucharadita de cúrcuma</li>
			<li>1 cucharadita de curry</li>
			<li>1 cucharadita de Ras el hanut (opcional)</li>
			<li>1 cucharada de cilantro picado</li>
			<li>1 cucharadita de chili seco picado</li>
			<li>Sal</li>
			<li>Aceite de oliva</li>
		</ul>
