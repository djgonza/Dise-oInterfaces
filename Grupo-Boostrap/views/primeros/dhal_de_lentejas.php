	<section  class="text-justify">
	<h4 class="c-verde">Modo de Realización</h4>
		<p>Empezaremos por cocer las lentejas con algo más del doble de agua que de lentejas.</p><p>Mientras las lentejas se cuecen, picamos la cebolla muy finita. Ponemos a calentar el aceite junto con los granos de comino. Cuando esté bien caliente, añadimos la cebolla y dejamos a fuego medio/bajo hasta que ablande y empiece a coger un poco de color.</p><p>En ese punto añadimos el tomate dejamos también que se haga poco a poco. Añadimos las especias y removemos. Sobre todo con el picante es mejor que vayáis añadiendo poco a poco para que no os paséis ni os quedéis cortos.</p><p>Habrán pasado unos 20 minutos y las lentejas ya estarán listas. Las añadimos a nuestra salsa de tomate.</p><p>Vamos aplastando poco a poco con una cuchara de madera y añadimos medio vaso de agua. Ponemos el fuego bajo y dejamos que se haga todo lentamente. Removemos constantemente y probamos para corregir de sal y especial al gusto. Dejamos que el agua se reduzca y listo.</p><p>Para acompañar el dhal de lentejas rojas podéis preparar también un arroz basmati hervido. Yo le suelo poner también un poco de cilantro para aromatizar.</p><p>¡Que aproveche este Dhal de lentejas rojas!</p>
