	<h4 class="c-verde">Modo de Realización</h4>
		<p>La clave para este keíbap vegano es el seitán. Para hacerlo ponemos en un recipiente 1 vaso de gluten de trigo y le añadimos las especias (ajo, perejil, pimentón de la vera y sal) y mezclamos.</p><p>Por otra parte ponemos medio vaso (o un tercio) de salsa de soja, le añadimos una cucharadita de salsa barbacoa (podéis usar humo si preferís) y mezclamos. Llenamos el resto del vaso con agua templada.</p><p>Mezclamos el gluten con el vaso de salsa de soja y agua y amasamos durante varios minutos. Formamos una bola y amasamos haciéndola más alargada. Envolvemos en film transparente apretando bien los extremos con un nudo. Quedará una especie de embutido. Pinchamos varias veces con un palillo y ponemos a cocer en caldo de verduras durante una hora más o menos.</p><p>Sacamos del caldo pasado este tiempo, quitamos el film y dejamos enfriar. Cortamos en lacas no muy gruesas y pasamos por la sartén con un poco de aceite de oliva. El sobrante lo volvemos a envolver en film transparente y guardamos en la nevera.</p><p>Para el resto del relleno del kebap de seitán, cortamos tomate en rodajas y lechuga en juliana (en tiras).</p><p>Para la salsa blanca podemos usar veganesa (aquí tenéis la receta) o salsa de yogur (la podéis encontrar en esta otra receta)</p><p>Para la salsa roja yo hago una mezcla de salsa de tomate o tomate triturado con una cucharada de ketchup, una pizca de orégano, una pizca de perejil y un poco de tabasco.</p><p>Montamos el kebap con la lechuga, el tomate y el seitán y añadimos las salsas al gusto.</p><p>¡Que aproveche!</p>
