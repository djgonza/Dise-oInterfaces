<h3>Tamaño del texto</h3>

<p>El diseño accesible de este sitio Web permite que el usuario pueda elegir el tamaño del texto que le  convenga. Esta acción puede llevarse a cabo de diferentes maneras según el navegador que se utilice o puede utilizar directamente los controles A+ y a- proporcionados en las páginas detalle.</p>

