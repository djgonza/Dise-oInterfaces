<h3>Sugerencias y reclamaciones sobre accesibilidad</h3>

<p>Si detecta que algún documento de este sitio web no es accesible o si tiene alguna otra duda en relación a la accesibilidad de este sitio web, puede utilizar el formulario de sugerencias que el Ayuntamiento de Madrid pone a su disposición.</p>

<h3>Lectura de contenidos</h3>

<p>En los contenidos finales informativos tales como Noticias, Actividades, etc. o información en PDF como Normativas o  BOAM, se incluye la posibilidad de escuchar el contenido de la página utilizando una herramienta sintetización de voz. Para ello puede utilizarse la tecla de acceso rápido 'E', o la botonera con las opciones habituales de "reproducir" "pausa" "parar" del componente "Escuchar" que está debajo del título en la zona central de la página.</p>



